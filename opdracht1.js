function helloWorld(hello){ 

	for(i=0; i< hello; i++){
		document.write("Hello World!" + "<br>");
	}
}
 
helloWorld(7);

 // De function heeft de naam helloWorld zodat je die makkelijk weer kan aanroepen. Het woord (hello) is voor de hoeveelheid en kan je terug vinden in de for loop. als variabel i =0 groter is dan hello doet hij wat er staat. En omdat ik de function 7 keer uit laat voeren staat er 7 keer Hello World!
