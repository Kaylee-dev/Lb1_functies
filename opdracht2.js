function addition(number1, number2) {
	return number1 + number2;
}

var sum1 = addition(10, 12);
document.write("10 + 12 = " + sum1 + "<br>");


function subtraction(number1, number2) {
	return number1 - number2;
}

var sum2 = subtraction(58, 34);
document.write("58 - 34 = " + sum2 + "<br>");


function multiplication(number1, number2) {
	return number1 * number2;
}

var sum3 = multiplication(6, 7);
document.write("6 + 7 = " + sum3 + "<br>");


function division(number1, number2) {
	return number1 / number2;
}

var sum4 = division(144, 12);
